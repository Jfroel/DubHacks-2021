public class Hour {
    private int id;
    private String task;
    private boolean existTask;

    public int getId() {
        return id;
    }
    public String getTask() {
        return task;
    }
    public boolean getExistTask() {
        return existTask;        
    }
    public void setId(int x) {
        id = x;
    }
    public void setTask(String s) {
        task = s;
    }
    public void setExistTask(boolean b) {
        existTask = b;        
    }

}