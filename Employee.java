public class Employee {
    private int id;
    private String name;
    private Week schedule;
    
    public int getId() {
        return id;
    }
    public String getName() {
        return name;
    }
    public Week getSchedule() {
        return schedule;
    } 
    public void setId(int x) {
        id = x;
    } 
    public void setName(String n) {
        name = n;
    }
    
}
